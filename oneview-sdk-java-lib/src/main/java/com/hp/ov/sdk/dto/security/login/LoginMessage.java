package com.hp.ov.sdk.dto.security.login;

import org.apache.commons.lang3.builder.EqualsBuilder;
import org.apache.commons.lang3.builder.HashCodeBuilder;
import org.apache.commons.lang3.builder.ToStringBuilder;

public class LoginMessage extends BaseLoginResource {

    private static final long serialVersionUID = 1L;

    private Boolean acknowledgment;
    private String message;

    /**
     * @return the acknowledgment
     */
    public Boolean getAcknowledgment() {
        return acknowledgment;
    }

    /**
     * @param acknowledgment
     *            the acknowledgment to set
     */
    public void setAcknowledgment(Boolean acknowledgment) {
        this.acknowledgment = acknowledgment;
    }

    /**
     * @return the message
     */
    public String getMessage() {
        return message;
    }

    /**
     * @param message
     *            the message to set
     */
    public void setMessage(String message) {
        this.message = message;
    }

    @Override
    public String toString() {
        return ToStringBuilder.reflectionToString(this);
    }

    @Override
    public boolean equals(Object obj) {
        return EqualsBuilder.reflectionEquals(this, obj);
    }

    @Override
    public int hashCode() {
        return HashCodeBuilder.reflectionHashCode(this);
    }

}
